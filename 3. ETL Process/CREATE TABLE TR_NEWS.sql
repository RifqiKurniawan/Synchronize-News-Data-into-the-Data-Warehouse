CREATE SEQUENCE  "TR_NEWS_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1;

CREATE TABLE TR_NEWS(
ID NUMBER DEFAULT "TR_NEWS_SEQ"."NEXTVAL",
CREATED_AT DATE,
UPDATE_AT DATE,
DELETE_AT DATE,
TITLE VARCHAR2(1000),
CONTENT VARCHAR2(4000),
PUBLICATION_DATE DATE,
AUTHOR VARCHAR2(500)
)
